#include "../../minishell.h"

int 	builtin_echo(char **cmd)
{
	int 	i;

	i = 1;
	while (cmd[i])
	{
		write(1, "-", 1);
		ft_putendl_fd(cmd[i], 1);
		i++;
	}
	return (0);
}